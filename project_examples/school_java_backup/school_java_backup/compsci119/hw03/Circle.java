public class Circle
{
    public static void main(String[] args)
    {
    
        double valueOfPi = 3.14159;
        double radius = 2;
        double radiusSquared = radius * radius;
        double area = valueOfPi * radiusSquared;
        
        
        System.out.println("The area of a circle with a radius of " + 
        radius + " is " + area);
    }
}



